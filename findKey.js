const assertEqual = require('./assertEqual');

const findKey = (obj, cb) => {
  for (let [key, value] of Object.entries(obj)) {
    if (cb(value) || cb(key))
      return key;
  }
};


const res = findKey({
  "Blue Hill": { stars: 1 },
  "Akaleri":   { stars: 3 },
  "noma":      { stars: 2 },
  "elBulli":   { stars: 3 },
  "Ora":       { stars: 2 },
  "Akelarre":  { stars: 3 }
}, x => x.stars === 2); // => "noma"

